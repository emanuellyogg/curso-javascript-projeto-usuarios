class User {
  //construtor, onde estarão os métodos, as ações que serão executadas
  constructor(name, gender, birth, country, email, password, photo, admin) {
    this.name = name;
    this.gender = gender;
    this.birth = birth;
    this.country = country;
    this.email = email;
    this.password = password;
    this.photo = photo;
    this.admin = admin;
  }
}